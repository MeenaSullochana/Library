                                    <h2 class="tpfaq__title text-danger">Frequently Asked Questions (FAQ )</h3>
                                    <p style="text-align: justify;"><b> </b>
                                    </p>
                                    <b>What is the purpose of the Transparent Book Procurement Portal?</b>
                                    <p>It serves as a digital platform established by the Directorate of Public Libraries, Government of Tamil Nadu , to streamline the procurement process by connecting publishers, publisher cum distributors, and distributors with public libraries in Tamil Nadu.</p>
                                    <b>Who is eligible to register on the portal?</b>
                                    <p>Publishers, publisher cum distributors, and distributors interested in supplying books to public libraries in Tamil Nadu are eligible to register.
                                    </p>
                                    <p><b>What information is required from publishers at the time of registration?</b></p>
                                    <p>Publishers must provide the year of establishment, total number of books published to date and in the last three years, lists of best seller books, top 5 translated books, if any, awarded titles, book categories, specialized categories, and the primary language of publication.</p>
                                    <p><b>What documents are needed for registration?
                                    </b></p>
                                    <p>Registration requires the latest book catalogue in the prescribed format and Publication Ownership documents. Authorization letters from publishers are needed for those registering as distributor cum publishers.
                                    </p>
                                    <p><b>Can publishers add books published by others?</b></p>
                                    <p>No, publishers are only allowed to add books they have published. Distributor or Distributor cum publisher can add Books published by others.</p>
                                    <p><b>Should information about subsidiary publications be disclosed? </b></p>
                                    <p>Yes, any subsidiary publications should be mentioned during the registration process.</p>
                                    <p><b>Is there an application fee for applying for books for procurement?</b></p>
                                    <p>Yes, there is a non-refundable application fee for each book submission to cover evaluation costs and ensure serious submissions. </p>
                                    <p><b>How many copies should be submitted for consideration for review and selection purposes?</b></p>
                                    <p>Archiving purpose: To comply with the Delivery of Books (Public Libraries) Act, 1954, send one copy of all published books to Connemara Public Library. Additionally, send one copy to state libraries such as Anna Centenary Library,Chennai and Kalaignar Centenary Library in Madurai for archiving of Books. </p>
                                    <p>Review purpose:  send two copies for review of books in Chennai for review. Address to be sent: Anna Centenary Library, Chennai. </p>
                                    <p><b>When does the book selection process conclude?</b></p>
                                    <p>The book selection process concludes by the end of each quarter, specifically by June 30, September 30, December 31, and March 31, coinciding with the deadlines for indent submissions.
                                    </p>
                                    <p><b>What are the consequences of submitting inaccurate information?</b></p>
                                    <p>Submitting false or misleading information can lead to application rejection or further action.</p>
                                    <p><b>Is a declaration required upon submission?</b></p>
                                    <p>Yes, a declaration verifying the accuracy and completeness of the provided information, along with all necessary attachments, must be submitted.</p>
                                    <p><b>What is the overall criteria used for book procurement?</b></p>
                                    <p>Selection is based on evaluations from subject experts, librarian and public user reviews, and reader forum recommendations.</p>
                                    <p><b>How is the book selection list organized?</b></p>
                                    <p>The book selection list is organized based on cumulative score points provided by the subject expert, Librarian, and user.</p>
                                    <p><b>What are the book procurement negotiation guidelines?</b></p>
                                    <p>
                                        <ul>
                                            <li>Procurement negotiations conducted online based on price index</li>
                                            <li>Publishers/applicants can negotiate discount rates online.</li>
                                            <li>Negotiation overseen by the Procurement Price Negotiation Committee</li>
                                            <li>Book prices are compared to similar books in the market. </li>
                                            <li>Entire process conducted online with no direct negotiations.</li>
                                        </ul>
                                    </p>
                                    <p><b>How are books selected for purchase by libraries?</b></p>
                                    <p>Books list is made available for purchase order by libraries</p>
                                    <p><b>How do libraries select books for purchase?</b></p>
                                    <p>Libraries select books based on budget allocation for each category of books. Chosen books are categorized according to budget, and the final book list is prepared with the approval of the readers' forum of the library.</p>
                                    <p><b>How long will the chosen books remain available in the portal for purchase by libraries?</b></p>
                                    <p>Two years.The books will be removed from the list in chronological order and archived.</p>
                                    <p><b>How long the purchase order received will be kept for supply for books?</b></p>
                                    <p>30 Days , if it is not delivered within the stipulated time , the purchase order will be automatically canceled.</p>
                                    <p><b>Is there a “central facility” for sending purchased books?</b></p>
                                    <p>Yes, there is a central facility in Chennai for collecting the purchased books, from where the delivery partner distributes the books to various libraries across Tamil Nadu.</p>
                                    <p><b>What is the waiting time period of receiving payment for the books supplied?</b></p>
                                    <p>10 Days, after verification of the received book by the respective libraries.</p>
                                    <p><b>How can I become a user reviewer for the book selection process?</b></p>
                                    <p>Must be a member of a readers' forum associated with any of the public libraries in Tamil Nadu. The libraries participating in the user review process are selected on a rotation basis from various types of libraries, including Village Libraries, Branch Libraries, Full-Time Branch Libraries, District Central Libraries, and State-Level Libraries. From these selected libraries, librarians and readers forum choose user reviewers. Both the libraries and user reviewers are rotated annually to ensure a diverse and comprehensive understanding of library users' preferences. To express your interest and potentially be selected as a user reviewer, you should communicate with the librarian at your local public library.</p>
                                    
